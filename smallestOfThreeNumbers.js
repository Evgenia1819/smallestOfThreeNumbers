function getSmallestNumber (firstNumber, secondNumber, thirdNumber) {
let smallestNumber = Math.min(firstNumber, secondNumber, thirdNumber);
return smallestNumber;

}

console.log(getSmallestNumber(2, 5, 3));
console.log(getSmallestNumber(600, 342, 123));
console.log(getSmallestNumber(25, 21, 4));